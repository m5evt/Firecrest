void displayFreq(uint32_t freq, uint8_t bat_level) {
	int millions;
	int millions2;
	int hundredthousands;
	int tenthousands;
	int thousands;
  int hundreds;
  int tens;
	
	uint32_t newFreq = freq/4;
	newFreq -= TXOFFSET;

	millions = (int)(newFreq/10000000);
	millions2 = ((newFreq/1000000)%10);
  hundredthousands = ((newFreq/100000)%10);
  tenthousands = ((newFreq/10000)%10);
  thousands = ((newFreq/1000)%10);
  hundreds = ((newFreq/100)%10);
  tens = ((newFreq/10)%10);

	if (millions == 0) {
    drawNumberBig(11,0);
  }
  else {
    drawNumberBig(millions,0);
  }
	drawNumberBig(millions2,BIGNUMbits);
	drawNumberBig(hundredthousands,((BIGNUMbits)*2)+5);
	drawNumberBig(tenthousands,((BIGNUMbits)*3)+5);		  
	drawNumberBig(thousands,((BIGNUMbits)*4)+5);
	drawNumberSmall(hundreds,(((BIGNUMbits)*5)+5), 2);  
	drawNumberSmall(tens,(((BIGNUMbits)*5)+SMALLNUMbits+5), 2);
				
	drawDot(BIGNUMbits*2);

	drawBat(bat_level);
}

