	millions = (int)(rxFreq/10000000);
	millions2 = ((rxFreq/1000000)%10);
  hundredthousands = ((rxFreq/100000)%10);
  tenthousands = ((rxFreq/10000)%10);
  thousands = ((rxFreq/1000)%10);
  hundreds = ((rxFreq/100)%10);
